package com.express.bean;

public class Info {
	private String boxnumber;
	private String company;
	private String phone;
	private String adress;
	private String put_time;
	private String radom_pass;
	private int status;
	

	
	public String getCompany() {
		return company;
	}
	public void setCompany(String company) {
		this.company = company;
	}
	public String getBoxnumber() {
		return boxnumber;
	}
	public void setBoxnumber(String boxnumber) {
		this.boxnumber = boxnumber;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getAdress() {
		return adress;
	}
	public void setAdress(String adress) {
		this.adress = adress;
	}
	public String getPut_time() {
		return put_time;
	}
	public void setPut_time(String put_time) {
		this.put_time = put_time;
	}
	
	public String getRadom_pass() {
		return radom_pass;
	}
	public void setRadom_pass(String radom_pass) {
		this.radom_pass = radom_pass;
	}

	
	public int getStatus() {
		return status;
	}
	public void setStatus(int status) {
		this.status = status;
	}
   
	
	
}
